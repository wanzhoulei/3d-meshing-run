"""
Loader for the sacrifice-moves tet mesh dataset (.npz format).

The dataset is generated by gen_sacrifice_dataset.py and stores bad meshes
obtained by random-walking from T_good, filtered to keep only instances
where the greedy baseline gets stuck well below T_good quality.

Storage format (numpy .npz, no object arrays):
  P            : (N, V, 3)    float64 — point coordinates per sample
  T_good       : (K0, 4)      int32   — shared reference good topology
  T_bad_flat   : (M, 4)       int32   — all bad topologies concatenated
  T_bad_sizes  : (N,)         int64   — number of tets per bad sample
  greedy_score : (N,)         float64 — softmin score of greedy-best state
  tgood_score  : (N,)         float64 — softmin score of T_good
"""
from __future__ import annotations

import numpy as np
from typing import Tuple


class SacrificeDataset:
    """
    Dataset of (P, T_bad) pairs where greedy refinement gets stuck below
    T_good quality — requiring sacrifice moves that the RL agent can learn.

    API mirrors TetMat73Dataset for drop-in compatibility:
      ds.get(i)         -> (P_i, T_bad_i)
      ds.num_samples    -> int
      ds.T_good         -> (K0, 4) int32
      ds.close()        -> no-op (no file handle held after __init__)
    """

    def __init__(self, filename: str):
        data = np.load(filename, allow_pickle=False)

        self.P: np.ndarray = data["P"]                   # (N, V, 3)
        self.T_good: np.ndarray = data["T_good"]         # (K0, 4)
        T_bad_flat: np.ndarray = data["T_bad_flat"]      # (M, 4)
        T_bad_sizes: np.ndarray = data["T_bad_sizes"]    # (N,)
        self.num_samples: int = int(self.P.shape[0])

        self.greedy_score: np.ndarray = (
            data["greedy_score"] if "greedy_score" in data else np.full(self.num_samples, np.nan)
        )
        self.tgood_score: np.ndarray = (
            data["tgood_score"] if "tgood_score" in data else np.full(self.num_samples, np.nan)
        )

        # Unpack concatenated T_bad into a list of variable-length arrays
        self._T_bad: list[np.ndarray] = []
        offset = 0
        for sz in T_bad_sizes.tolist():
            sz = int(sz)
            self._T_bad.append(T_bad_flat[offset : offset + sz])
            offset += sz

    def get(self, i: int) -> Tuple[np.ndarray, np.ndarray]:
        """Return (P_i, T_bad_i). P is (V, 3) float64, T is (Ki, 4) int32."""
        i = int(i)
        if not (0 <= i < self.num_samples):
            raise IndexError(i)
        return self.P[i].copy(), self._T_bad[i].copy()

    def close(self) -> None:
        """No-op — dataset is fully loaded into memory at construction."""
        pass
